insert into 财务分析综合表
select
    a.报告时间,
    a.公司代码,
    a.报告类型,
    a.公司简称,
    a.公司名称,
    a.办公地址,
    a.行业分类,
    a.在职员工的数量合计,
    a.支付给职工及为职工支付的现金,
    a.应付职工薪酬 as [应付职工薪酬（期末余额）],
    b.应付职工薪酬 as [应付职工薪酬（期初余额）],
    round(replace(a.营业收入,',','')/replace(a.在职员工的数量合计,',','')/10000,0) as [员工平均工资（万）],
    round((replace(a.支付给职工及为职工支付的现金,',','') + replace(a.应付职工薪酬,',','') - replace(b.应付职工薪酬,',',''))/replace(a.营业收入,',',''),4) as 员工工资占营业收入比率,
    a.营业收入,
    a.归属于上市公司股东的净利润,
    a.归属于上市公司股东的扣除非经常性损益的净利润,
    a.经营活动产生的现金流量净额,
    a.归属于上市公司股东的净资产,
    a.总资产,
    round(replace(a.经营活动产生的现金流量净额,',','')/replace(a.归属于上市公司股东的净资产,',',''),4) as 净资产比率,
    round(replace(a.归属于上市公司股东的净利润,',','')/replace(a.总资产,',',''),4) as 资本利润率,
    a.[现金分红金额（含税）],
    a.现金分红总额（含其他方式）占合并报表中归属于上市公司普通股股东的净利润的比率,
    b.归属于上市公司股东的净资产 as  [归属于上市公司股东的净资产（上期）],
    b.总资产 as [总资产（上期）],
    round(replace(a.营业收入,',','')/replace(a.在职员工的数量合计,',','')/10000,4) as [人均营业额（万）],
    round(replace(a.归属于上市公司股东的净利润,',','')/replace(a.在职员工的数量合计,',','')/10000,4) as [人均净利润（万）],
    round(replace(a.经营活动产生的现金流量净额,',','')/replace(a.归属于上市公司股东的净利润,',',''),4) as 经营活动产生的现金流量净额占净利润的比率,
    round((replace(a.营业收入,',','') - replace(b.营业收入,',',''))/replace(b.营业收入,',',''),4) as 营业收入增长率,
    round((replace(a.归属于上市公司股东的净利润,',','') - replace(b.归属于上市公司股东的净利润,',',''))/replace(b.归属于上市公司股东的净利润,',',''),4) as 归属于上市公司股东的净利润增长率,
    round((replace(a.归属于上市公司股东的扣除非经常性损益的净利润,',','') - replace(b.归属于上市公司股东的扣除非经常性损益的净利润,',',''))/replace(b.归属于上市公司股东的扣除非经常性损益的净利润,',',''),4) as 归属于上市公司股东的扣除非经常性损益的净利润增长率,
    round((replace(a.经营活动产生的现金流量净额,',','') - replace(b.经营活动产生的现金流量净额,',',''))/replace(b.经营活动产生的现金流量净额,',',''),4) as 经营活动产生的现金流量净额增长率,
    a.营业成本,
    a.投资收益,
    a.三、营业利润,
    a.四、利润总额,
    a.所得税费用,
    a.五、净利润,
    a.销售费用,
    a.管理费用,
    a.财务费用,
    a.研发费用,
    --"无形资产-内部研发","资产减值准备","折旧和摊销","所得税税率",
    a.期末总股本,
    a.固定资产,
    a.在建工程,
    --c.无形资产-土地使用权",
    a.投资性房地产,
    a.商誉,
    a.预收款项,
    a.应付票据及应付账款,
    a.应付账款,
    a.预付款项,
    a.应收账款,
    a.应收票据,--"应收票据（上期）",
    --流动资产合计 NUMERIC,
    a.流动负债合计,
    a.存货,
    a.货币资金,
    a.短期借款,
    a.一年内到期的非流动负债,--"有息债券(短期借款＋一年内到期的非流动负)","应收票据增加额（本期应收票据余额-上期应收票据余额）",
    a.长期借款,
    a.应付债券,--"流动比率","速动比率","有息负债"
    a.流动资产合计,--"负债合计",
    a.销售商品、提供劳务收到的现金,
    a.[六、期末现金及现金等价物余额],
    a.五、现金及现金等价物净增加额--f.现金收入,
    --round(replace(a.营业收入,',','')/replace(b.在职员工的数量合计,',',''),0) as 人均营业收入
from 财务分析基础表 a 
left join 财务分析基础表 b
where a.报告时间 - b.报告时间 = 1
and a.报告类型 = b.报告类型
and a.公司代码 = b.公司代码;

